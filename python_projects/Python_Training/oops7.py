class Student:
    def setid(self, id):
        self.id = id

    def getid(self):
        return self.id

    def setName(self, name):
        self.name = name

    def getName(self):
        return self.name

    def setAdd(self, address):
        self.address = address

    def getAdd(self):
        return self.address

    def setMarks(self, marks):
        self.marks = marks

    def getMarks(self):
        return self.marks

